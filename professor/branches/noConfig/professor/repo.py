from os.path import join

_repopath = "."

def setPath(path):
    _repopath = path

def getPath():
    return _repopath

def getMCPath():
    return join(getPath(), "mc")

def getRefPath():
    return join(getPath(), "ref")

def getIpolPath():
    return join(getPath(), "ipol")

def getMCScanPath():
    return join(getPath(), "mc-scan")

def getMinPath():
    return join(getPath(), "min")
